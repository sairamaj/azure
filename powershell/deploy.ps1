<#
      This will create virtual machine and other required networking
      taken directly from: https://docs.microsoft.com/en-us/powershell/azure/get-started-azureps?view=azurermps-4.0.0#create-a-linux-virtual-machine

      This shows using powershell but usually it is better to use JSON configuration file and New-AzureRmResourceGroupDeployment powershell command
